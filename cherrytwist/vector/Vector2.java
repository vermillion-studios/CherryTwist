package cherrytwist.vector;

public class Vector2 {
	private int x, y;

	public Vector2(int x, int y) {
		this.x = x;
		this.y = y;
	}

	/**
	 * returns the x value of this vector.
	 * @return x
	 */
	public int getX() {
		return x;
	}

	/**
	 * returns the y value of this vector.
	 * @return y
	 */
	public int getY() {
		return y;
	}
}
